        String sqlQuery = String.format(blankQuery, 
                maSinhVienTF.getText(),
                hoVaTenTF.getText(),
                gioiTinhTF.getText(),
                lopTF.getText(),
                khoaTF.getText(),
                queQuanTF.getText());
